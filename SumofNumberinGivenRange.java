public class SumofNumberinGivenRange {
    public static void main(String[] args) {
        int a = 1;
        int b = 20;
        int sum = 0;
        for (int i = a; i <= b; i++) {
            sum = sum + i;
        }
        System.out.println("The Sum of  Natural Natural in given range are" + " " + sum);
    }
}
